var Todo = mongoose.model('Todo', {
    text: {
        type: String,
        required: true,
        minlength: 1,
        trim: true
    },
    completed: {
        type: Boolean,
        default: false
    },
    completedAt: {
        type: Number,
        default: null
    }
});



var newTodo = new Todo({
    text: 'Cook Dinner',
    completed: false,
    completedAt: 12
});